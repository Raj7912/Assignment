class Area_of_sec
{
public static void main(String[] args)
{
	double pi=3.142,r=12.0,angle,radian,area;
	angle=30;
	radian=30*(pi/180);
	area=0.5*radian*r*r;
	System.out.println("Area of a sector with radius "+r+" and angle "+angle+" which is in radian = "+radian+" is "+area);
	
}



}
